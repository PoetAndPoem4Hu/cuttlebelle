'use strict';


//--------------------------------------------------------------------------------------------------------------------------------------------------------------
// Dependencies
//--------------------------------------------------------------------------------------------------------------------------------------------------------------
import BasePath from 'path';


//--------------------------------------------------------------------------------------------------------------------------------------------------------------
// Local
//--------------------------------------------------------------------------------------------------------------------------------------------------------------


/**
 * Wrapping the path standard lib to unify directory separators
 *
 * @type {Object}
 */
export const Path = {
	...BasePath.posix,
	sep: '/',
	normalize: ( input /*: string */ ) /*: string */ => BasePath.posix.normalize( input.replace(/\\/g, '/') ).replace(/\\/g, '/'),
};
